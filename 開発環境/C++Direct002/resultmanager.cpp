//==========================================================================
// リセット
//==========================================================================
void CResultManager::Reset(void)
{
	// 値のクリア
	memset(&m_nScoreNum[0], 0, sizeof(m_nScoreNum));	// スコアの種類
	m_nBaseScore = 0;		// スコア
	m_nToatalScore = 0;	// 最終スコア
}

