{
	
}

//==========================================================================
// スコア加算
//==========================================================================
void CResultManager::AddScore(int nValue)
{
	m_nBaseScore += nValue;
	m_nToatalScore += nValue;
}

//==========================================================================
// スコアの種類加算
//==========================================================================
void CResultManager::AddScoreNum(CResultManager::ADDTYPE type)
{
	// 引数の種類を加算
	m_nScoreNum[type]++;

	// 値加算
	switch (type)
	{
	case CResultManager::ADDTYPE_NONE:
		break;

	case CResultManager::ADDTYPE_NORMAL:
		m_nToatalScore += mylib_const::COMBOBONUS_1;
		break;

	case CResultManager::ADDTYPE_BLUE:
		m_nToatalScore += mylib_const::COMBOBONUS_2;
		break;

	case CResultManager::ADDTYPE_GOLD:
		m_nToatalScore += mylib_const::COMBOBONUS_3;
		break;
	}
}

//==========================================================================
// 種類毎の数取得
//==========================================================================
int CResultManager::GetScoreTypeNum(ADDTYPE type)
{
	return m_nScoreNum[type];
}

//==========================================================================
// スコアの取得処理
//==========================================================================
int CResultManager::GetBaseScore(void)
{
	return m_nBaseScore;
}

//==========================================================================
// スコアの取得処理
//==========================================================================
int CResultManager::GetToatalScore(void)
{
	return m_nToatalScore;
}
